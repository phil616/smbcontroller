package smb

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
	"sync"
	"text/template"
	"time"
)

type GlobalSection struct {
	Workgroup         string
	ServerString      string
	NetbiosName       string
	ServerMinProtocol string
	ServerMaxProtocol string
}

type ShareSection struct {
	ShareName  string
	Path       string
	Comment    string
	Browseable bool
	GuestOK    bool
	ForceGroup string
	ValidUsers []string
	WriteList  []string
	ReadList   []string
	DenyAll    bool
}

type ConfigData struct {
	GeneratedAt string
	GlobalSection
	Shares []ShareSection
}

type Generator struct {
	mu             sync.Mutex
	confPath       string
	backupDir      string
	backupMaxCount int
}

func NewGenerator(confPath, backupDir string, backupMaxCount int) *Generator {
	if backupMaxCount <= 0 {
		backupMaxCount = 5
	}
	return &Generator{confPath: confPath, backupDir: backupDir, backupMaxCount: backupMaxCount}
}

func (g *Generator) Render(data ConfigData) (string, error) {
	if data.GeneratedAt == "" {
		data.GeneratedAt = time.Now().In(time.Local).Format(time.RFC3339)
	}
	sort.Slice(data.Shares, func(i, j int) bool { return data.Shares[i].ShareName < data.Shares[j].ShareName })
	var buf bytes.Buffer
	tpl, err := template.New("smb.conf").Funcs(template.FuncMap{"join": strings.Join}).Parse(smbConfTemplate)
	if err != nil {
		return "", err
	}
	if err := tpl.Execute(&buf, data); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func (g *Generator) Write(data ConfigData) error {
	g.mu.Lock()
	defer g.mu.Unlock()
	content, err := g.Render(data)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(g.confPath), 0750); err != nil {
		return err
	}
	if g.backupDir != "" {
		if err := g.backupCurrent(); err != nil {
			return err
		}
	}
	tmpPath := g.confPath + ".tmp"
	if err := os.WriteFile(tmpPath, []byte(content), 0600); err != nil {
		return err
	}
	if err := validateWithTestparm(tmpPath); err != nil {
		if removeErr := os.Remove(tmpPath); removeErr != nil {
			return fmt.Errorf("%w; failed to remove temporary config %s: %v", err, tmpPath, removeErr)
		}
		return err
	}
	return os.Rename(tmpPath, g.confPath)
}

func (g *Generator) ReadCurrent() (string, error) {
	b, err := os.ReadFile(g.confPath)
	if err != nil {
		return "", err
	}
	return string(b), nil
}

func (g *Generator) backupCurrent() error {
	if _, err := os.Stat(g.confPath); err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return err
	}
	if err := os.MkdirAll(g.backupDir, 0700); err != nil {
		return err
	}
	src, err := os.ReadFile(g.confPath)
	if err != nil {
		return err
	}
	backupPath := filepath.Join(g.backupDir, "smb.conf.bak."+time.Now().In(time.Local).Format("20060102150405"))
	// #nosec G703 -- backupDir is a trusted local service configuration value; filename is generated by the service.
	if err := os.WriteFile(backupPath, src, 0600); err != nil {
		return err
	}
	return g.pruneBackups()
}

func (g *Generator) pruneBackups() error {
	entries, err := os.ReadDir(g.backupDir)
	if err != nil {
		return err
	}
	var names []string
	for _, entry := range entries {
		if !entry.IsDir() && strings.HasPrefix(entry.Name(), "smb.conf.bak.") {
			names = append(names, entry.Name())
		}
	}
	sort.Strings(names)
	for len(names) > g.backupMaxCount {
		if err := os.Remove(filepath.Join(g.backupDir, names[0])); err != nil {
			return err
		}
		names = names[1:]
	}
	return nil
}

func validateWithTestparm(path string) error {
	if _, err := exec.LookPath("testparm"); err != nil {
		return nil
	}
	// #nosec G204 -- path points to the service-generated temporary smb.conf and is passed as an argv value, not through a shell.
	out, err := exec.Command("testparm", "-s", path).CombinedOutput()
	if err != nil {
		return fmt.Errorf("smb.conf validation failed: %w: %s", err, strings.TrimSpace(string(out)))
	}
	return nil
}

const smbConfTemplate = `# Generated by SMB Controller - DO NOT EDIT MANUALLY
# Last updated: {{.GeneratedAt}}

[global]
    workgroup = {{.Workgroup}}
    server string = {{.ServerString}}
{{- if .NetbiosName}}
    netbios name = {{.NetbiosName}}
{{- end}}
{{- if .ServerMinProtocol}}
    server min protocol = {{.ServerMinProtocol}}
{{- end}}
{{- if .ServerMaxProtocol}}
    server max protocol = {{.ServerMaxProtocol}}
{{- end}}
    security = user
    map to guest = bad user
    log level = 1
    max log size = 1000
    passdb backend = tdbsam
{{- range .Shares}}

[{{.ShareName}}]
    path = {{.Path}}
    comment = {{.Comment}}
    browseable = {{if .Browseable}}yes{{else}}no{{end}}
    guest ok = {{if .GuestOK}}yes{{else}}no{{end}}
    read only = yes
{{- if .ForceGroup}}
    force group = {{.ForceGroup}}
{{- end}}
{{- if .ValidUsers}}
    valid users = {{join .ValidUsers " "}}
{{- else if .DenyAll}}
    valid users = __smb_controller_no_access__
{{- end}}
{{- if .WriteList}}
    write list = {{join .WriteList " "}}
{{- end}}
{{- if .ReadList}}
    read list = {{join .ReadList " "}}
{{- end}}
    create mask = 0664
    directory mask = 0775
    force create mode = 0664
    force directory mode = 2775
{{- end}}
`
